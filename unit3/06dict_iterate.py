
dict_1 = {'Sam': 'football',
          'Tom': 'swimming',
          'Jason': 'basketball',
          'Alice': 'badminton'}
print(dict_1)
print()

for a in dict_1:
    print(a)
print()

for a in dict_1.keys():
    print(a)
print()

for a in dict_1.values():
    print(a)
print()

for (a, b) in dict_1.items():
    print(a, b)
print()



